#pragma once

#include "XArch.h"
#include "XArchFrameLowering.h"
#include "XArchISelLowering.h"
#include "XArchInstrInfo.h"
#include "XArchSelectionDAGInfo.h"
#include "XArchSubtarget.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "XArchGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class XArchSubtarget : public XArchGenSubtargetInfo {
  virtual void anchor();

private:
  const DataLayout DL;       // Calculates type size & alignment.
  XArchInstrInfo InstrInfo;
  XArchTargetLowering TLInfo;
  XArchSelectionDAGInfo TSInfo;
  XArchFrameLowering FrameLowering;
  InstrItineraryData InstrItins;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  XArchSubtarget(const Triple &TT, StringRef CPU,
               StringRef FS, XArchTargetMachine &TM);

  /// getInstrItins - Return the instruction itineraries based on subtarget
  /// selection.
  const InstrItineraryData *getInstrItineraryData() const override {
    return &InstrItins;
  }
  const XArchInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const XArchRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const XArchTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const XArchFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const XArchSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
};
} // End llvm namespace
