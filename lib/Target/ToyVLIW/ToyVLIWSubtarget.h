#pragma once

#include "ToyVLIW.h"
#include "ToyVLIWFrameLowering.h"
#include "ToyVLIWISelLowering.h"
#include "ToyVLIWInstrInfo.h"
#include "ToyVLIWSelectionDAGInfo.h"
#include "ToyVLIWSubtarget.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "ToyVLIWGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class ToyVLIWSubtarget : public ToyVLIWGenSubtargetInfo {
  virtual void anchor();

private:
  const DataLayout DL;       // Calculates type size & alignment.
  ToyVLIWInstrInfo InstrInfo;
  ToyVLIWTargetLowering TLInfo;
  ToyVLIWSelectionDAGInfo TSInfo;
  ToyVLIWFrameLowering FrameLowering;
  InstrItineraryData InstrItins;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  ToyVLIWSubtarget(const Triple &TT, StringRef CPU,
               StringRef FS, ToyVLIWTargetMachine &TM);

  /// getInstrItins - Return the instruction itineraries based on subtarget
  /// selection.
  const InstrItineraryData *getInstrItineraryData() const override {
    return &InstrItins;
  }
  const ToyVLIWInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const ToyVLIWRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const ToyVLIWTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const ToyVLIWFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const ToyVLIWSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);
};
} // End llvm namespace
